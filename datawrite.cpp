static int
get_low_bit(int offset)
{	
	int res = 0xA;
	for (int i=offset; i<offset + 5; ++i) {
		for (int j=7; j>=0; --j) {
			if (memory[i] & (1 << j) )
				res = std::min(res, j);
		}
	}
	return res;
}

void 
write_char(int x, int y, int pos, int low, int high, uint8_t screen[][MENU_SCREEN_WIDTH])
{
	for(int i=0; i<MAX_FONT_WIDTH; ++i){
		for(int j=7, col = 0; j>=0; --j, ++col){
			if (memory[pos + i] & (1<<j)){
				screen[y+i][x+col] = high;
			} else {
				screen[y+i][x+col] = low;
			}
		}
	}
}

void 
write_line(int x, int y, const char *msg, int low, int high, uint8_t screen[][MENU_SCREEN_WIDTH])
{
	int char_m;
	for (; *msg && (MENU_SCREEN_WIDTH - x) >= MAX_FONT_WIDTH; ++msg) {

		if (*msg == ' ') {

			for (int i=0; i<WORD_SPACE; ++i)
				write_char(x, y, 36 * 5, low, high, screen);

			x += WORD_SPACE;

		} else {


			if (*msg < 'A')
				char_m = (*msg - '0') * 5;
			else 
				char_m = (*msg - 'A' + 10) * 5;

			write_char(x, y, char_m, low, high, screen);
			x += (0x7 - get_low_bit(char_m)) + ROW_CHAR_SPACE + 1;
		}
	}
}

void
write_data(menu_screen_data& data, uint8_t screen[][MENU_SCREEN_WIDTH])
{
	memset(screen, 0, sizeof(screen[0][0]) * MENU_SCREEN_HEIGHT * MENU_SCREEN_WIDTH);
	static int maxrow = 0;

	for (int i=0; i<maxrow; ++i) {

		for (int j=0; j<MENU_SCREEN_WIDTH; ++j)
			screen[i][j] = logo[i][j];
	}

	maxrow = std::min(maxrow + 1, MENU_SCREEN_HEIGHT );
	int magic = MAX_FONT_WIDTH + COL_CHAR_SPACE;

	for(int i=data.offset, y=magic; i<data.content.size(); ++i, y+=magic) {

		if (MENU_SCREEN_HEIGHT - y < magic)
			break;

		std::string towrite = " " + data.content[i];
		write_line(40, y, towrite.c_str(), (i>data.offset)?0: 2 , 1, screen);
	}
}